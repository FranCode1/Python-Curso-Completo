#3. Ciclo de Vida - Proyecto de software critico:
# Debes liderar el desarrollo de un sistema de control para un puente levadizo que permite el paso de barcos y vehiculos.
# Un error en el sistema podria provocar accidentes graves, con riesgo para vidas humanas.
# Se debe cumplir con normas internacionales de seguridad y certificaciones estrictas.

#Pregunta:
# - Que modelo de ciclo de vida utilizarias y por que?
# - Explica como manejarias la verificacion y validacion en este modulo.



#Respuesta: 
# Usaría el modelo en V porque es el más adecuado para sistemas críticos de seguridad, ya que cada fase de especificación y diseño tiene su correspondiente fase de pruebas, lo que asegura trazabilidad, control de calidad y facilita la certificación.

# En verificación, aplicaría revisiones de requisitos, pruebas unitarias, integración y métodos formales como especificación en B, asegurando que el software esté libre de ambigüedades y errores lógicos.

# En validación, haría pruebas en entornos simulados y de campo, considerando fallos y escenarios extremos, para comprobar que el sistema cumple realmente con su propósito: garantizar la seguridad del puente y de los usuarios.