# 🌍 Proyecto Módulo 7:

### **Gestor de Tareas con API, Base de Datos y Automatización**

# 📌 Objetivo:

# * Crear un **gestor de tareas** que:

#   1. Permita **crear y listar tareas** desde la terminal.
#   2. Guarde las tareas en una **base de datos SQLite**.
#   3. Exporte los datos a **JSON**.
#   4. Obtenga **inspiración/motivación desde una API externa**.
#   5. Se ejecute como un **script automatizado**.
#   6. Respete **buenas prácticas (PEP8, modularidad)**.

# ---

## 📝 Estructura de archivos

# ```
# proyecto_modulo7/
# │── gestor_tareas.py        # Script principal
# │── db.py                   # Manejo de la base de datos
# │── utils.py                # Funciones de utilidad (JSON, API, etc.)
# │── requirements.txt        # Dependencias externas (requests)
# ```

